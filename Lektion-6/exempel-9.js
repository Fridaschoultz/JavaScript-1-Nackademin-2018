// a) Skapa en funktion som beräknar medelvärdet av två tal.
function medel2(x1 , x2){
    return (x1+x2) / 2;  
}
console.log(medel2(2,4));

// b) Skapa en funktion som beräknar medelvärdet av tre tal.
function medel3(x1,x2,x3){
    return (x1+x2+x3) / 3
}
console.log(medel3(5,10,25));
